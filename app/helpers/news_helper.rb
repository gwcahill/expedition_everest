require 'rubygems'
require 'open-uri'
require 'simple-rss'

module NewsHelper
  def news_feed
    rss = SimpleRSS.parse(open('http://feeds.feedburner.com/disneyparks').read)

    rss.items.first(5) do |i|
      content_tag(:p, "**************Hello world!***********")
      puts "================================================================================="
      puts "================================================================================="
      puts "================================================================================="
      puts "================================================================================="
      puts "================================================================================="
      puts "Title: #{i.title}"
      puts "Link: #{i.link}"
      link_to("body", "#{i.link}", html_options = {})
      puts "================================================================================="
      puts "================================================================================="
      puts "================================================================================="
      puts "================================================================================="
      puts "================================================================================="
    end
  end
end